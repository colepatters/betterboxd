<p>
    My name is Cole, and I created betterboxd to finally publish <i>something</i>. I was 
    growing tired of the endless web-developer cycle of starting projects that never quite
    came to fruition. My plan here was to create just the minimum product, so that I never
    got caught up in what I like to call "scope-creep" that has plagued my career in software
    development.
</p>

<br>

<h3 class="h3">
    Why betterboxd?
</h3>

<p>
    Recently, I've changed my media consumption habits to better match my girlfriend's
    love for movies. Since I'm a nerd and always need to track things, I remembered that
    Letterboxd existed, and that I had an old account. After booting that thing back up,
    I noticed that it was exactly how I remembered, <i>but not in a good way.</i> Overrun
    by ads and outdated design, I felt that someone needed to make a better version of this.
    And then, <i>eureka!!!</i> I'm a web developer, and I need a project to work on, why
    not me?
</p>

